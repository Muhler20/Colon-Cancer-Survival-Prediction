# import necessary libraries required for data analysis, visual representation
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# import preprocessing libraries
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.impute import SimpleImputer
from sklearn.decomposition import PCA

# import libraries required for model creation
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix

# hide warnings if any
import warnings
warnings.filterwarnings('ignore')

# load dataset
df_crc = pd.read_csv("colorectal_cancer_prediction.csv")

print("ROWS & COLUMNS COUNT OF DATASET:", df_crc.shape)
print("\nBelow are top five rows of dataset:")
print(df_crc.head())

# basic information of dataset: missing values and it's count, statistics, data types
print("\nDATASET INFORMATION:")
print(df_crc.info())

print("\nMISSING VALUES COUNT:")
print(df_crc.isnull().sum())

print("\nSTATISTICAL SUMMARY OF DATASET:")
print(df_crc.describe())

# exploratory data analysis
# finding numerical columns
numerical_cols = df_crc.select_dtypes(include=['int64','float64']).columns

# finding categorical columns
categorical_cols = df_crc.select_dtypes(include=['object']).columns


# exploring distribution of numerical variables
df_crc[numerical_cols].hist(figsize=(14,10), bins=20)
plt.suptitle("Distribution of Numerical Variables")
plt.show()


# exploring distribution of categorical variables
for col in categorical_cols:
    plt.figure(figsize=(6,4))
    sns.countplot(data=df_crc, x=col)
    plt.title(f"Distribution of {col}")
    plt.xticks(rotation=45)
    plt.show()


# exploring survival distribution
if "Survival_Status" in df_crc.columns:
    plt.figure(figsize=(6,4))
    sns.countplot(data=df_crc, x="Survival_Status")
    plt.title("Survival Status Distribution")
    plt.show()


# exploring age vs survival
if "Age" in df_crc.columns and "Survival_Status" in df_crc.columns:
    plt.figure(figsize=(7,5))
    sns.boxplot(x="Survival_Status", y="Age", data=df_crc)
    plt.title("Age vs Survival")
    plt.show()


# exploring BMI vs survival
if "BMI" in df_crc.columns and "Survival_Status" in df_crc.columns:
    plt.figure(figsize=(7,5))
    sns.boxplot(x="Survival_Status", y="BMI", data=df_crc)
    plt.title("BMI vs Survival")
    plt.show()


# exploring cancer Stage vs survival
if "Cancer_Stage" in df_crc.columns and "Survival_Status" in df_crc.columns:
    plt.figure(figsize=(7,5))
    sns.countplot(data=df_crc, x="Cancer_Stage", hue="Survival_Status")
    plt.title("Cancer Stage vs Survival")
    plt.show()

# exploring correlation matrix: relation between every variables
df_corr = df_crc.copy()

for col in df_corr.select_dtypes(include='object').columns:
    df_corr[col] = df_corr[col].astype('category').cat.codes

plt.figure(figsize=(12,8))
sns.heatmap(df_corr.corr(), cmap="RdBu_r")
plt.title("Correlation Matrix of Colorectal Cancer Dataset")
plt.show()

# data cleaning: removing unnecessary columns
if "Patient_ID" in df_crc.columns:
    df_crc = df_crc.drop(columns=["Patient_ID"])

# data imputation: filling missing values
numerical_cols = df_crc.select_dtypes(include=['int64','float64']).columns
categorical_cols_cols = df_crc.select_dtypes(include=['object']).columns

numerical_imputer = SimpleImputer(strategy="median")
categorical_imputer = SimpleImputer(strategy="most_frequent")

df_crc[numerical_cols] = numerical_imputer.fit_transform(df_crc[numerical_cols])
df_crc[categorical_cols] = categorical_imputer.fit_transform(df_crc[categorical_cols])

# encode categorical values by changing them into numeric values
label_encoders = {}

for col in categorical_cols:
    le = LabelEncoder()
    df_crc[col] = le.fit_transform(df_crc[col])
    label_encoders[col] = le

# outlier analysis using quartiles
for col in numerical_cols:
    q1 = df_crc[col].quantile(0.25)
    q3 = df_crc[col].quantile(0.75)
    iqr = q3 - q1

    lower = q1 - 1.5 * iqr
    upper = q3 + 1.5 * iqr

    df_crc[col] = np.where(df_crc[col] < lower, lower, df_crc[col])
    df_crc[col] = np.where(df_crc[col] > upper, upper, df_crc[col])

# separating target feature
X = df_crc.drop("Survival_Status", axis=1)
y = df_crc["Survival_Status"]

# normalizing data
scaling = StandardScaler()
X_scaled = scaling.fit_transform(X)

# dimensionality reduction
pca = PCA(n_components=0.95)
X_pca = pca.fit_transform(X_scaled)

print("Original Features:", X.shape[1])
print("Reduced Features:", X_pca.shape[1])

# split data for training and testing
X_train, X_test, y_train, y_test = train_test_split(
    X_pca, y, test_size=0.2, random_state=42
)

# create model using random forest classifier algorithm, three parameters are passed
rfc_model = RandomForestClassifier(
    n_estimators=200,
    max_depth=12,
    random_state=42
)

rfc_model.fit(X_train, y_train)

# perform prediction
y_pred = rfc_model.predict(X_test)

# evaluating model using classification report/metrics
print("\nMODEL ACCURACY:", accuracy_score(y_test, y_pred))

print("\nCLASSIFICATION REPORT:")
print(classification_report(y_test, y_pred))

print("\nCONFUSION MATRIX")
print(confusion_matrix(y_test, y_pred))

# determine importance of features
X_train2, X_test2, y_train2, y_test2 = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42
)

rfc_feature_imp = RandomForestClassifier(n_estimators=200, random_state=42)
rfc_feature_imp.fit(X_train2, y_train2)

importances = rfc_feature_imp.feature_importances_

feature_importance = pd.DataFrame({
    "Feature": X.columns,
    "Importance": importances
}).sort_values(by="Importance", ascending=False)

print("\nTop Important Features")
print(feature_importance.head(10))

# plot feature importances
plt.figure(figsize=(10,6))
sns.barplot(x="Importance", y="Feature", data=feature_importance.head(15))
plt.title("Top 15 Features Affecting Survival")
plt.show()